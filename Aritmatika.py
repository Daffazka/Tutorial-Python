#OPERASI ARITMATIKA

a = 10
b = 3

#OPERASI PERTAMBAHAN (+)
operasi = a+b
print(a, '+', b, '=', operasi, type(operasi))

#OPERASI PENGURANGAN (-)
operasi = a - b
print(a, '-', b, '=', operasi, type(operasi))

#OPERASI PERKALIAN (*)
operasi = a * b
print(a, '*', b, '=', operasi, type(operasi))

#OPERASI PEMBAGIAN (/)
operasi = a / b
print(a, '/', b, '=', operasi, type(operasi))

#OPERASI EKSPONEN / PERPANGKATAN (**)
operasi = a ** b
print(a, '**', b, '=', operasi, type(operasi))

#OPERASI MODULUS / HASIL BAGI(%)
operasi = a % b
print(a, '%', b, '=', operasi, type(operasi))

#OPERASI FLOOR DIVISION (//)
operasi = a // b
print(a, '//', b, '=', operasi, type(operasi))