print("Data float = ", data_float)
print("Status data tersebut adalah = ", type(data_float))

data_str = "jiancokk"
print("Data string = ", data_str)
print("Status data tersebut adalah = ", type(data_str))

data_bool = False
print("Data boolean = ", data_bool)
print("Status data tersebut adalah = ", type(data_bool))
